<h4>Day 2:</h4>

<div class="items">

<div class="activity">
	<span class="subtitle">Activity</span>
	 
<ul>
	
	<li>Divide the class into three teams.</li>
	 <li>One student from each team goes to the board. Call out an infinitive of an English verb. Students race to write the correct principal parts.</li>
	<li>As they are doing this hold up one of the cards that was made for the giant floor time line with the name of a tense on it.</li>
	<li>The remaining team members must use the given verb in that tense in an English sentence.</li>
	<li>1 point to the team that finishes first at the board, and 1 point for each team with a correct sentence.</li>
	<li>During the last five minutes of the game, give teams turns to win extra points. Call out an infinitive to a team and give them 10 seconds to say the principal parts. Then give the next team a turn. Just a quick finale.</li>
	
</ul>


