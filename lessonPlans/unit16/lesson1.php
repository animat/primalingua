	<span class="subtitle">Activity</span>

<ul>
<li>List three different types of present.
<br />
<em>e.g. I eat. I am eating. I do eat.</em></li>
<li>They are all happening now, but we use them in different ways.</li>
<li>Ask students to think up situations in which they would use these three different types.
<br />
<em>e.g. Dogs eat bones.
