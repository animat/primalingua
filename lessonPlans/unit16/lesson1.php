<div class="discuss">
	<span class="subtitle">Discuss</span>
	 
<ul>
<li>Draw a simple time line on the board with a point in the middle which is labeled present.</li>
<br />
<img src="present-tense.gif">
<br />
<li>Everything to the left of present has happened before now. It is the past.</li>
<li>Everything to the right of the present point is still to happen. It is the future.</li>
<li>Ask students if they have encountered a time line in math or history. Compare and contrast them.
<br />
<em>e.g. counting backwards in numbers as you move left, going farther forward in history as you move to the right.</em></li>
<li>The tense of a verb tells when the verb is being done. The present tense is used for an action that is happening now.</li>
<li>There are six tenses that are common to most languages. English has twelve variations of these six tenses.</li>
</ul>

</div>

