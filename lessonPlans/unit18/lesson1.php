<div class="activity">
	<span class="subtitle">Activity</span>
	 
<ul>
<li>Ask the class to brainstorm words that ask a question.
	<br />
	<em> e.g. who? when? where? what? why? how? which? </em>
	</li>
	<li>Students sit facing a partner. One student will be the interrogator and the other will be the responder. The interrogator will ask his partner one question using each of the question words. The responder will answer in full sentences. You can give them a topic such as "Your last birthday" or "Your favorite restaurant."</li>
	<li>Have them write down exactly what they say, questions and answers.</li>
	<li>When they are finished they should underline or circle the question words and do the same to the words that were in direct response to the question word.
		<br /><br />
	<em>e.g. Where did you have your birthday party?
	<br />       I had my party in the garden.
	<br /><br />
	     Which cake did you want?
	     I wanted the chocolate cake.</em>
</li>

</ul>
	
</div>

<div class="discuss">
	<span class="subtitle">Discuss</span>

<ul>
<li>In Latin to ask = rogare. Interrogation and interrogator are derivatives with the prefix inter = between. An interrogation = an asking between two people.</li>
<li>Interrogative words come in different parts of speech:
<br />	 pronoun:  who? whom? what?
<br />	 adjective:  which?
<br />	 adverb:  where? when? how? why?</li>
<li>The words that were underlined in the responses should give a clue as to the part of speech of the interrogative word used.
<br />	 If the underlined response is a noun or pronoun, then the question word that  replaces it is an interrogative pronoun.
<br />	 If the underlined response is an adjective, then the question word was an  interrogative adjective.
<br />	 If the underlined response is a prepositional phrase, an adverb, or a whole clause,  then the question word was an interrogative adverb.</li>
</ul> 

