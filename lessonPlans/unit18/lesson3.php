<div class="dicsuss">
	<span class="subtitle">Discuss</span>
		
		<ul>
		<li>We signal that the statement has become a question on paper with the question mark, but when it is spoken we signal that it is a question by changing the intonation of our voice.</li>
		<li>Questions can be made in three ways:
		<br />
		   inverting the helping verb and subject
		<br /> 
		  using an interrogative word and inversion
		<br />  
		 changing the intonation of the voice without inversion of the verb</li>
		
		<li>English puts its signal for a question at the end of the sentence, so if there is no interrogative word and no inversion, there is no warning until the end of the sentence that this is a question.</li>
		<li>Spanish and Latin have signals at the beginning of the sentence as well as the end to indicate that this is a question. You have a warning at the beginning that you will have to use a questioning intonation.
		 <br /> Spanish: inverted question mark at the beginning  <em>¿ qué es éso?</em>
		 <br /> Latin: -ne at the end of the first word, if there is no interrogative word. <em>Amatne ranas?</em></li>
		<li>Why would inversion not work in Latin? There are no helping verbs. Everything is built into one form.</li>
	</ul>
