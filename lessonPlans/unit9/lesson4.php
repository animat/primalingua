<div class="activity">
<span class="subtitle">Activity</span>
Assign a Latin noun to each student. (They know about 25 nouns
 at this point.) <br /><br />

The student writes the subject form/nominative case on one side of a piece of paper in big letters and the direct object form/accusative case on the other side.<br /><br /> 


You make up a sentence in English using nouns you know they have. Again use only the verb to love. <br /><br />


The students who have the nouns that you used in your sentence come forward and put their piece of paper on the floor next to the verb amat. 
They will have to decide if their noun is being used as a subject or direct object, and they will have to decide which form of their noun to put face up. <br />
It does not matter where they put their noun because the word order does not matter. <br />
Ask them questions about the case, declension, and function of their nouns. <br />
Try more complex English sentences in which the subject is not the first word in the sentence or use other verbs. 
