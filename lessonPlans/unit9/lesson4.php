<div class="items">

Draw two suitcases on the board: one to hold all the endings that are for subjects, th eother to hold the new direct object endings. (n.b. we are not learning the plural endings for direct objects.)    THe suitcases will be named the Nominative Case and the Accusative Case to make the point that the case is a way to group endings that show the functions of the nouns. 

<div class="discuss">
<span class="subtitle">Discuss</span>

<ul>
<li>Latin nouns belong to the same group, the same declension, if
 they have the same endings. <br />

So all the words that attach -a for subjects and -am for direct objects belong to 1st declension. <br />
All the words that attach -us / -r for subjects and -um for direct objects belong to 2nd declension. </li>
<li>The endings themselves belong to groups based on their functions.<br /> 
All endings that are used for subjects (-a and -us / -r) are in a group <br /> 
and <br /> 
all endings that are used for direct objects ( -am and -um) are in a group.<br />  
These groups are called cases.</li> 
<li>The case for subjects is called the nominative case.</li>  
<li>The case for direct objects is called the accusative case.
</li>  

</ul>
