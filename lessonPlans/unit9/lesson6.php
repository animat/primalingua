<ul>

<li>What type of verb is est / sunt?    
  It is not doing an action either alone or to something else; 

so it is neither a transitive nor an intransitive verb. </li>
<li>This verb, the verb to be, explains that one thing is equal to another. <br />
 The girl is a student.  The girl = a student.<br /> 
 They are small.  They = small.</li> 
<li>This type of verb is called a linking verb, because it links the subject to something equal to it.</li> 
<li>Just as the two things on either side of an equal side must be equal in math, so in language they must be equal. 
If the subject is in the nominative case then what it equals on the other side of the linking verb must be in the nominative case. </li>
<li>This type of nominative that comes after a linking verb is called a predicate nominative. 
 
 
</li>

</ul> 
