	<span class="subtitle">Play</span>
 <a href="http://www.primalingua.net/lessonPlans/files/09-stripGame.doc">Strip Sentence Game</a>
  Cut the attached papers into strips. Line strips up on a table or floor in the classroom. <br />

Students work in pairs, come and grab the first sentence strip, run back to their seats and answer all the questions about the underlined word. <br />
One team member runs up to you, positioned at a desk in the front of the room to correct it. They will need to form a line as you work quickly. <br />
Assign two points if all answers are correct. Send them back to fix anything wrong. 1 point when everything is eventually correct. <br />
Then they work to translate the sentence and again run it up for correction. <br />
Again 2 points for correct the first time and 1 point when it is eventually correct. <br /><br />


Tips: students may not move to next strip until the first is correct. <br />
One strip at a time. No using books or notes. <br />
If they are absolutely stuck, start giving hints.<br /> 
The point is instant feedback, everyone is working, they work till they get it correct. <br />
No more than two people working together. 
