<div class="activity">
	<span class="subtitle">Activity</span>
	
Create a variety of LinguaZone.com games using the verb forms and spend a day playing in class or allowing students to play in a computer lab. Assign for homework as well.

</div>

<div class="activity">
	<span class="subtitle">Activity</span>
	
Everyone stands in a circle. Choose a Latin verb to conjugate in the circle. One person starts and says 1 sg. of the verb. The next person in the circle says 2 sg.  The seventh person will start with 1 sg. again. You can designate a person as the switcher.
<br />
When the conjugating gets around to him, he will change to another Latin verb and carry on with the next person and number. 
<br />Try to keep a steady beat.
<br />
When someone makes a mistake (or after they get good, if someone breaks the beat) he steps out of the circle.
<br />
Continue until it is a face off between two students and the rest will be cheering on to see who is the first to stumble.

</div>

<div class="activity">
	<span class="subtitle">Activity</span>
	
<ul>

<li>Three teams. Each team has desks pulled together and a piece of paper. One member from each team is at the board.</li>
<li>The teacher calls out a verb form in English. The students at the board race to write it correctly in Latin.
<br />
<em>e.g. we fight - pugnamus</em>
<br />
Students win a point for their team by being the first to write the form correctly on the board.</li>
<li>The same students stay at the board while the teacher does the same thing, different forms but the same verb. The same students stay at the board for three or four forms.
<br />
<em>e.g.  I fight - pugno
<br />they fight - pugnant
<br />to fight - pugnare</em></li>
<li>Meanwhile back at the desks, the remaining team members are taking the first verb form that was called out, pugnamus, and writing a Latin sentence with English translation using that form.
<br />
<em>e.g. Pugnamus cum equo sub luna. We fight with a horse under the moon.</em>
<br />
There can be requirements as to how many words must be in the sentence.</li>
<li>After the students at the board are finished, the rest of the team puts the sentence on the board. Three points for any correct sentences.</li>
<li>Three new students come to the board for the next round.</li>

</ul>

