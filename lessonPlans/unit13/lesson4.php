Concentration:
<ul>

<li>Students are in pairs. Give them a whole class period to become proficient at this game, and then let the pairs show off to the class.</li>
<li>Partners sit on the floor facing each other with legs crossed.</li>
<li>One partner is designated as the English person and the other as the Latin person. You can call out "Switch" every few minutes.</li>
<li>They start the rhythm of Concentration: slap one leg, then the other, snap the fingers on one hand, then the other. Repeat.</li>
<li>Using only the verb to love, the Latin partner says a form on each snap:
<br />
<em>amas, amant</em></li>
<li>Keeping the rhythm of Concentration, on the snaps the English partner translates the form from the last snap and chooses a form randomly for her last snap:
<br />
<em>they love, we love</em></li>
<li>Keeping the rhythm of Concentration, the Latin partner translates the form from the last snap and chooses a form randomly for her last snap:
<br />
<em>amamus, amo</em>
<br />
One student's last word is being translated by the partner, and so on...
<br /></li>
<li>I have had classes who have wanted to do this for days. You can make it more difficult for those who master it with the verb to love.
<br />  - give partners a new verb to try
<br /> - allow partners to switch verbs randomly as they switch forms.
	
</ul>

