<div class="Activity">
	<span class="subtitle">Activity</span>

<em>Pass the Word</em>
<br />
This is a quick three to five minute game that can be used anytime to start class or to pick up the tempo when things are dragging.<br />
Everyone stands in a circle and something is tossed, a ball or a frog.<br />
Start by saying a Latin vocabulary word while tossing the ball to a student in the circle. That student catches the ball and says the translation of the word while tossing it back to you.<br />
Have the ball tossed back to you every time until they are good at this. Then they can say the answer while they catch the ball and choose a new word while tossing the ball to another student.<br />
You can go from English to Latin or Latin to English. 
Later this game can be played with changing words from singular to plural, adjectives from masculine to feminine, subject to direct object forms, anything.<br />


		  
</div>

<div class="discuss">
	<span class="subtitle">Discuss</span>

<ul>
<li>Write every vocab word, old and new, on an index card and deal cards out to students. Have students come up to the board one by one and write the Latin nouns. They have to put all of the nouns that end in -a in one list and all that end in -us in another list.</li>
<li>Define declension: a group of nouns that have the same endings.</li>
<li>Label the first list 1st declension and the second list 2nd declension.</li>
<li>Look again at the list of Latin nouns in the vocabulary section. Instead of identifying them by gender, now have the students identify them by declension.</li>

</ul>


</div>

<div class="discuss">
	<span class="subtitle">Discuss</span>
	<ul>

<li>New "weird" vocabulary words</li>
<li>-r is another 2nd declension ending. The words puer and vir are masculine and belong to 2nd declension.</li>
<li>The last three words belong to 1st declension because they have the same endings as the words in that group, but they are masculine words. (These are exceptions, but it makes the point that there is a difference between gender and declension.)</li>
<li>An adjective agreeing with any of these new words will have to be masculine.
<br />
<em>    e.g. vir magnus, agricola malus</em>
<br />
They do not look the same, but they agree. </li>



	</ul>
</div>


