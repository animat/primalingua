<li>Conjugate <strong>to see</strong>.</li>
<li>The present is regular, but the past is irregular, a completely different word, saw.</li>
<li>Have students conjugate the verbs <strong>to do</strong> and <strong>to have</strong> on the board in simple present and past tenses.</li>
<li>In addition to the past being irregular, the present of each is slightly irregular in 3 sg. (although they retain the regular -s inflection).</li>
<li>But some verbs are very irregular, most of the forms are different words. What do you think is the most irregular English verb?</li>
<li>Conjugate <strong>to be</strong>.</li>
<li>None of the simple present or past forms even resemble the infinitive.</li>
</ul>

