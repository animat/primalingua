<div class="discuss">
	<span class="subtitle">Discuss</span>

<ul>
<li>The verb <strong>to be</strong> is the most irregular verb in most languages.</li>
<li>Why might this be the most common irregular verb? It is the most commonly used verb.</li>
<li>Perhaps something like the Whisper-Down-the-Lane theory happened with this verb. Since it is the most used, it is subject to the most change.</li>
<li>The verbs <strong>to have</strong> and <strong>to do</strong> are other frequently used verbs and common irregular verbs, although they are not nearly as irregular as <strong>to be</strong>.</li>
