		<li>The first language humans produced, most likely 
 the grunting of the cavemen, was oral.
</li>
		<li> The sounds of words may have been the imitation of sounds from nature or the sounds made from expressions of human emotions: fear, surprise
 </li>
<li>As more complex oral language developed, there developed a need to write it down.
  - to keep records
  - to record events
  - to preserve fame through recording deeds and thoughts. Some think the Greeks believed that they would achieve the immortality that fame brings only through the written word.
</li>
<li>Written language takes different forms
  - pictograms and ideograms representing objects directly and ideas represented by those objects
  - logograms, phonograms, or syllabaries representing sounds of names of objects and combined to make words
  - alphabets with letters representing individual sounds, the most versatile way of representing thousands of words
</li>
<li>Written language usually represents the oral language, but sometimes a spoken language dies away and the script used to write it remains.      e.g. hieroglyphs and cuneiform
</li>
<li>Two forms of Chinese, Mandarin and Cantonese, use the same written language, but the oral languages are very different. Someone speaking Mandarin and Cantonese could share something written, but would not understand each other when speaking. 
</li></ul>
