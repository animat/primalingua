		<h4>Day 2:</h4>
		<div class="items">
			<div class="activity">
				<span class="subtitle">Activity</span>
				Experiment and see how language could change when it passes from one generation or one group of people to the next. Play whisper down the lane with a nonsense word. 
				<ul>
					<li>Number each stident around the room.</li>
					<li>The teacher goes out into the hall with a nonsense word, such as sarguintifial, written on a piece of paper.</li>
					<li>The teacher calls one student out into the hall, says the word and shows it to the student. </li>
					<li>That student then stays out in the hall while the teacher returns to the class. The student in the hall is writing down the word from memory, right after seeing it from the teacher, but without looking at it, while the teacher sends the next student out into the hall.</li>
					<li>Student #1 says the word and shows it to student #2, who then stays, writes it down, and prepares to say it and show it to student #3.</li>
					<li>The last student will then write the word up on the board, announce it to the class, and compare it to the original from the teacher.</li>
					<li>Have students list the words they wrote down in order on the board to see how gradual the changes were. Make sure the list of numbers is already on the board.</li>
					<li>Look for changes such as consonants dropping out, vowels changing, the end of the word dropping off, the word splitting into two. </li>
				</ul>
			</div>
